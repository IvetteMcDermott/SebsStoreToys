
    function toggleO(){
       let div = document.getElementById('full-orders')
       if (div.style.display === 'none') {
        div.style.display = 'block';
       }else {
        div.style.display = 'none';
       }
    }
    function toggleB(){
       let div = document.getElementById('bookmarks')
       if (div.style.display === 'none') {
        div.style.display = 'block';
       }else {
        div.style.display = 'none';
       }
    }
